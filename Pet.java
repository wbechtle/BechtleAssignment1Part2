public class Pet {

    // Attributes
    private String name;
    private int age;

    // Constructor
    public Pet(String name, int age) {

        this.name = name;
        this.age = age;
    }
    // Getters
    public String getName() {
        return this.name;
    }
    public int getAge() {
        return this.age;
    }
}
